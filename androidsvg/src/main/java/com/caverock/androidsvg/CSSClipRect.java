package com.caverock.androidsvg;

public class CSSClipRect {
    Length top;
    Length right;
    Length bottom;
    Length left;

    CSSClipRect(Length top, Length right, Length bottom, Length left) {
        this.top = top;
        this.right = right;
        this.bottom = bottom;
        this.left = left;
    }
}
